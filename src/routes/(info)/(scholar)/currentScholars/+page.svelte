<h3 class="h3">Biography</h3>
<p>
	Nelly Rosario is a visiting scholar in Comparative Medis Studies at MIT and is the author of Song
	of the Water Saints: A Novel, winner of a 2002 PEN/Open Book Award. She earned an Master of Fine
	Arts from Columbia University and a Bachelor of Science in Environmental Engineering from MIT.
	Honors include the Sherwood Anderson Award in Fiction, NALAC Artist Grants, National Arts Club
	Writing Fellowships, and a Hurston/Wright Award in Fiction. Her fiction, nonfiction, and poetry
	appear in various anthologies and publications, including Callaloo, Review, Meridians, The New
	York Times, and el diario/La Prensa, where her bi-weekly column has run since 2008. Rosario is an
	assistant professor in the MFA Program at Texas State University- San Marcos.
</p>
<p>
	Currently Rosario is preparing for her next novel, which concentrates on medicine and anatomy, by
	visiting MIT libraries, interviewing people, and sitting in on lectures. She notices changes since
	her own student days including many more women students and faculty and a greater focus on digital
	thinking, multimedia, new ways of telling stories. Rosario also serves part-time as writer and
	researcher for the ongoing Blacks at MIT History Project directed by Clarence Williams, MIT
	adjunct professor of urban studies, emeritus. “We’re looking at how blacks influenced the
	Institute and vice versa, collecting oral histories, photographs and data, and analyzing the
	material within a larger framework and context. We’re thinking about a way to look at history to
	unfold the future, what that means for the Institute, and how diversity and excellence can work in
	tandem.”
</p>

