<h3 class="h3">Biography</h3>
<p>
	Prof. Ribbeck obtained her Bachelor’s degree and her PhD in Biology from the University of
	Heidelberg, Germany. She continued her postdoctoral research at the European Molecular Biology
	Laboratory, Heidelberg, Germany, and the Department of Systems Biology, Harvard Medical School.
	Katharina Ribbeck established her independent research group as a Bauer Fellow at the FAS Center
	for Systems Biology, Harvard University in 2007, and joined the Department of Biological
	Engineering at MIT as an Assistant Professor in 2010.
</p>
<h3 class="h3">Research</h3>
<p>
	The Laboratory for Biological Hydrogels’ focus is on basic mechanisms by which mucus barriers
	exclude, or allow passage of different molecules and pathogens, and the mechanisms pathogens have
	evolved to penetrate mucus barriers. It hopes to provide the foundation for a theoretical
	framework that captures general principles governing selectivity in mucus, and likely other
	biological hydrogels such as the extracellular matrix, and bacterial biofilms. The Lab’s work may
	also be the basis for the reconstitution of synthetic gels that mimic the basic selective
	properties of biological gels.
</p>

