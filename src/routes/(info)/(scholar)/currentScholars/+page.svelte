<h3 class="h3">Research Focus</h3>
<p>
	Aberrant cell migration is a hallmark of several invasive diseases, such as metastatic cancer and
	systemic autoimmune disorders. I am particularly interested in the regulation of cell motility and
	the underlying intracellular signaling processes as modulated by interaction with the
	cytoskeleton. My current research combines biochemical, cell biological and systems biological
	engineering approaches to elucidate the intracellular signaling mechanism underlying increased
	breast cancer metastasis due to expression of an invasion-specific protein, MenaINV.
</p>
<h3 class="h3">Teaching Goals</h3>
<p>
	Hands-on experience at the bench is useful for all engineers, experimentalists and theoreticians
	alike. In biological engineering, understanding how to experimentally perturb cell-based systems
	in a systematic and controlled manner is paramount to applying an engineering perspective across
	disciplines. My aim as an instructor of 20.109 is to provide practical, interesting, and
	motivating experiences that promote rewarding Aha! moments and drive novel insight and innovation.
	I believe that mentoring at the personal level, as well as the instructional level, is key to
	preparing the next generation of bioengineers for their careers tackling global technical health
	challenges. As such, I strive to provide opportunities for all students to improve and succeed in
	performing and communicating their science.
</p>

