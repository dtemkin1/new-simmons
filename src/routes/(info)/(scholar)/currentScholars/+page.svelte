<h3 class="h3">Biography</h3>
<p>
	In the Fall of 2007, Dr. Robert M. Randolph became MIT’s first Chaplain to the Institute. An
	ordained minister, he is responsible for the chaplaincy program at MIT and teaches a class each
	year dealing with the Abrahamic Faiths: Judaism, Christianity and Islam. He serves on the Board of
	Ministry at Harvard University and is a member of the Clergy Chapter of the Memorial Church on the
	Harvard Campus. Dr. Randolph came to MIT in 1979 as the Assistant Dean for Student Affairs. He
	served as Dean in Residence from 1982 until 1986, and in 1993, he became the Senior Associate Dean
	for Undergraduate Education and Student Affairs. In 1994-1995, he helped bring to culmination, a
	process that created a Religious Activities Center which is home, along with the Chapel built in
	1956, for the religious traditions represented on the MIT campus. In recognition of his
	contribution to the Institute in 1996, he received the Gordon Y. Billard Award for Exemplary
	Service to MIT. Dr. Randolph is currently the Housemaster of Bexley Hall. He also serves on the
	Board of Ministry of Harvard University, is a Trustee of the Stephen Phillips Memorial Charitable
	Trust Scholarship Fund, and serves on the board of the Rockport Chamber Music Festival.
</p>
<p>
	According the Bob, "The biggest challenge with regard to religious life at MIT is simply keeping
	people talking to each other, so that the stereotypes that operate, and have operated for far too
	long out there, are not allowed to reimpose themselves."
</p>
