<h2 class="h2">Reservation Bylaws</h2>
<p>
	Contact our Reservations Chair at <a class="anchor" href="mailto:simmons-reservations@mit.edu">
		simmons-reservations@mit.edu</a
	> for more information.
</p>
<hr />
<ol class="list-decimal list-outside ml-6 py-2">
	<li>
		The Reservation Chair will deal with public space reservations. Public spaces are found on the
		first floor and east side of the second floor. Specifically, the reservation chair will
		coordinate reservations of the multi-purpose Room (MPR), dining room, conference room, 2nd floor
		study area, Athena lounge, mailbox lounge, night caf&eacute; lounge, and kitchen.
		<ol class="list-decimal list-outside ml-6 py-2">
			<li>
				The night caf&eacute; business must be informed of reservations in the night caf&eacute;
				lounge during operating times.
			</li>
			<li>
				Catering by Simmons Dining or the night caf&eacute; business should be encouraged,
				especially if the event uses the dining hall or night caf&eacute; area.
			</li>
		</ol>
	</li>
	<li>
		Reservations will be taken from the following types of groups and approved by the indicated
		party.
		<ol class="list-decimal list-outside ml-6 py-2">
			<li>
				Simmons only: Reservation Chair unless the event is large or long*, in which case the House
				Committee.
			</li>
			<li>
				Simmons person directly involved in planning an MIT group event: Reservation Chair unless
				the event is large or long, in which case the House Committee.
			</li>
			<li>
				MIT group with predominantly MIT** attendees: Reservation Chair unless the event is large or
				long, in which case the House Committee.
			</li>
			<li>
				MIT group with many non-MIT attendees: Reservation Chair unless the event is large or long,
				in which case the House Committee.
			</li>
			<li>
				No MIT group affiliation
				<ol class="list-decimal list-outside ml-6 py-2">
					<li>
						Simmons resident requesting reservation: Reservation Chair unless the event is large or
						long, in which case the House Committee.
					</li>
					<li>MIT student requesting reservation: House Committee.</li>
					<li>non-MIT affiliated person requesting reservation: House Committee.</li>
				</ol>
			</li>
			<li>Residential Scholar Program: Reservation Chair.</li>
		</ol>
		<p>
			* Large means more than 49 people. Long means more than 4 hours.<br />
			** MIT attendees must have a close affiliation with MIT (e.g., students, alumni, staff, and faculty).
		</p>
	</li>
	<li>
		Advance reservations
		<ol class="list-decimal list-outside ml-6 py-2">
			<li>
				Events organized by official MIT groups (e.g., alumni organization, administration) and
				Simmons government or house-team member sponsored events can be made up to one year in
				advance.
			</li>
			<li>
				Simmons groups can begin to make reservations for fall semester May 1st, IAP November 1st,
				and spring semester December 1st.
			</li>
			<li>
				All other groups can begin to request spaces up to 6 weeks in advance of the next academic
				term.
			</li>
			<li>
				Requests for events not open to all Simmons residents must be received at least a week
				before the event.
			</li>
			<li>
				Reservations must be received and approved at least 24 hours before the event. However,
				spontaneous use (see below) may be possible.
			</li>
		</ol>
	</li>
	<li>
		Spontaneous use
		<ol class="list-decimal list-outside ml-6 py-2">
			<li>
				Individuals and groups (less than 50 people) led by a Simmons resident who will be present
				throughout the event can use any of the available reservable spaces. Larger spontaneous
				events should be coordinated by Simmons house members and aimed at Simmons residents.
			</li>
			<li>First come, first served. Residents should work out any conflicts on their own.</li>
			<li>If damage occurs and a responsible party can be determined, fines may be issued.</li>
		</ol>
	</li>
	<li>
		Prohibited reservation times
		<ol class="list-decimal list-outside ml-6 py-2">
			<li>
				Reservation requests for events that take place during campus preview weekend, Rush, REX, or
				freshman orientation should be rejected unless they are FOR these programs.
			</li>
			<li>
				Reservation requests for events that take place during finals should be rejected unless they
				are finals or study-break oriented and only for Simmons residents.
			</li>
			<li>
				Reservations for the dining hall should be rejected if they interfere with student dining
				hours.
			</li>
			<li>No loud events should take place between the hours of 1 AM and 10 AM.</li>
			<li>House meetings have priority Sunday evenings 9-10:30 PM.</li>
		</ol>
	</li>
	<li>
		Fees and forms
		<ol class="list-decimal list-outside ml-6 py-2">
			<li>
				Costs related to setup/breakdown labor, housekeeping, and damages will be charged to the
				responsible person or group by the Simmons Hall House Manager.
			</li>
			<li>Income from bookings will be placed in the House Committee account.</li>
			<li>
				Fees may occasionally be charged for large or external groups. Contact the reservations
				chair (<a class="anchor" href="mailto:simmons-reservations@mit.edu"
					>simmons-reservations@mit.edu</a
				>) for more information.
			</li>
			<li>
				A Simmons Hall Space Reservation Application needs to be completed and delivered to the
				reservation chair before an event takes place if the event is registered through MIT, the
				group is paying a fee, there is a moderate risk of damage, or the reservation chair wants a
				written agreement. If no MIT account number is available, a deposit of $200 is required.
			</li>
			<li>
				If MIT registration is required, the group must complete this process before the event.
			</li>
		</ol>
	</li>
	<li>
		Notices
		<ol class="list-decimal list-outside ml-6 py-2">
			<li>
				A list of reservations should be posted online and at desk. Reasonable effort should be made
				to ensure that the notices are current.
			</li>
		</ol>
	</li>
</ol>
<hr />
<p>
	These are guidelines and will not cover every eventuality. The Reservation Chair and Committee
	should use their best judgment or consult the House Committee when these bylaws are insufficient.
</p>
