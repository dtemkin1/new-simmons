				Non-Live-In House Support Positions
				<ol class="list-decimal mx-4 px-4 py-2">
					<li>
						Individuals who play significant support roles within the Simmons Hall community but are
						not residents of Simmons Hall may include the House Manager, staff serving under the
						House Manager, the dining hall staff, and others. These individuals are not considered
						Members of the House, nor do they have any official standing with respect to the House,
						nor do they have any official responsibilities to the House. Nonetheless, the House,
						through its officers, is encouraged to communicate them, and ask for their support in
