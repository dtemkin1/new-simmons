<ol class="list-[upper-roman] mx-4 px-4 py-2">
	<li>
		<p class="font-bold uppercase">General Definitions</p>
		<ol class="list-decimal mx-4 px-4 py-2">
			<li>
				Constitution of Simmons Hall
				<ol class="list-decimal mx-4 px-4 py-2">
					<li>
						The purpose of the Constitution is: (a) to establish the overall model for governance of
						the House; (b) to define the roles of some of the individuals and groups who are
						involved in the governance of the House; (c) to define some of the procedures that guide
						the proper functioning of House governance.
					</li>
				</ol>
			</li>

			<li>
				The House
				<ol class="list-decimal mx-4 px-4 py-2">
					<li>
						The House refers to the collective residents and communities residing within the Simmons
						Hall dormitory of the Massachusetts Institute of Technology.
					</li>
				</ol>
			</li>

			<li>
				House and Chair Policies
				<ol class="list-decimal mx-4 px-4 py-2">
					<li>
						House and Chair Policies supplement the Constitution in guiding House governance, and
						may be created and revised continually over time.
					</li>
					<li>
						House Policies may include, but are not limited to, policies on the use of House
						facilities and House funds, rules of conduct such as quiet hours or regulations on the
						use of alcohol, policies that establish the roles and responsibilities of new House
						governance groups or positions, and policies that define or clarify House governance
						procedures.
					</li>
					<li>
						Chair Policies are determined by each current chair. These policies serve to clarify
						chairs' roles and responsibilities as assigned by the constitution. They also serve to
						inform members of the House of officers roles and responsibilities. The content of each
						chair's policies can be changed by said chair, however these changes must be announced
						at a House Meeting to take effect. It is encouraged that a chair solicit house opinion
						before changing his or her policies.
					</li>
				</ol>
			</li>
		</ol>
	</li>

	<li>
		<p class="font-bold uppercase">The House</p>
		<ol class="list-decimal mx-4 px-4 py-2">
			<li>
				Introduction
				<ol class="list-decimal mx-4 px-4 py-2">
					<li>
						The House makes many decisions that impact all its residents. It raises funds to use
						towards House-shared resources and events. The governance of Simmons Hall is established
						on the principle that when an issue affects all residents, every resident should have a
						voice in making a decision on that issue.
					</li>
				</ol>
			</li>

			<li>
				House Membership
				<ol class="list-decimal mx-4 px-4 py-2">
					<li>
						Every official resident of Simmons Hall is a member of the House. Each member may cast
						one vote in any House decision.
					</li>
				</ol>
			</li>

			<li>
				Role and Responsibilities of the House
				<ol class="list-decimal mx-4 px-4 py-2">
					<li>
						The House, by collective action, makes all major house-wide decisions and expresses the
						official opinion of Simmons Hall on any matter of interest to the residents of Simmons
						Hall.
					</li>
					<li>
						The House is responsible for: (a) establishing House and Chair Policies, and making
						changes to standing House Policies and the Constitution; (b) allocating House funds to
						be used towards House-shared resources and events; (c) discussing and deciding upon any
						issue of relevance to Simmons Hall or to MIT at large; (d) approving any resolution
						expressing the official opinion of the residents of Simmons Hall on any such issue (e)
						electing House Officers to carry out the will of the House.
					</li>
				</ol>
			</li>

			<li>
				House Procedures
				<ol class="list-decimal mx-4 px-4 py-2">
					<li>
						All official business of the House is discussed at meetings that are open and announced
						to all members of the House and occur at least once within every two-week period during
						spring and fall semesters that MIT classes are in session. House Officer meetings shall
						be held at least a half an hour before general House meetings.
					</li>
					<li>
						Any House member may propose an item to be discussed or decided upon by the House by
						submitting a proposal to the Chair of the House, who must place that item on the agenda
						of the next House meeting if the agenda for that meeting has not been set.
					</li>
					<li>
						For all proposals brought before the House, four actions are available to the House:
						Approve, Reject, Table, and Move-to-Full-Forum. The Chair of the House should remind
						participants at the start of each semester that these options are available. Voting may
						take place during meetings of the House or by ballot in the case of Move-to-Full-Forum.
						No item may be voted upon until after it has been discussed at a House meeting.
					</li>
					<li>
						At least one author of the proposal must be present to advocate for it. Should no author
						be present, the proposal shall be Tabled automatically, unless an author has presented
						the House Chair an alternative advocate for the proposal in advance. Would the same
						proposal be automatically tabled in this manner for the third consecutive time, it will
						instead be automatically rejected to avoid cluttering the agenda with orphaned
						proposals.
					</li>
					<li>
						During debate on a proposal, motions may be raised. After a member of the House states
						intention to raise a motion, another member must second the motion in order for it to
						have any effect (i.e. "be raised").
					</li>
					<li>
						Proposals may be approved during a House meeting following a motion to vote. Once
						seconded, the House chair should direct and tally a vote, unless a motion to object is
						raised and seconded. In such cases, the House should return to debate on the proposal.
						Proposals are approved by a majority vote yes, with the exception that proposals for
						Constitutional Amendments require a two-thirds majority vote for approval.
					</li>
					<li>
						Proposals may be approved during a House meeting following a motion to vote. Once
						seconded, the House chair should direct and tally a vote, unless a motion to object is
						raised and seconded; in such cases, the House should return to debate on the proposal.
						At the conclusion of every such vote, the House Chair must impose an increase in speed
						of the meeting at his/her discretion, ranging from one (1) to five (5) percent.
						Proposals are approved by a majority vote yes, with the exception that proposals for
						Constitutional Amendments require a two-thirds majority vote for approval.
					</li>
					<li>
						In the case where those assembled feel they are unanimously in favor of approving the
						proposal, a motion to white-ballot can be used. When a motion to white-ballot is raised,
						the House chair should ask those assembled if any are opposed. If any member of the
						House present is opposed, then the motion fails and the House returns to Debate. Once a
						motion to white-ballot fails for a particular measure, no further motions to
						white-ballot may be raised for that measure. A motion to white-ballot may not be used to
						approve a constitutional amendment or a proposal greater than $300.
					</li>
					<li>
						A motion to force-vote may be used to counter the case when a vote cannot be held due to
						frivolous objections. Once raised in the course of normal debate, a motion to force-vote
						causes the House Chair to poll the support for a vote of those assembled. This vote
						cannot be interrupted by a motion to object or any other motion except for the explicit
						exceptions outlined in 2.4.10 and 2.4.12. If the force-vote passes with majority support
						then the meeting should proceed as if a motion to vote had been raised, with the
						exception that no motions, including motions to object, may interrupt the vote. For each
						proposal, no more than one motion to force-vote may be raised in any five minute period.
					</li>
					<li>
						Proposals are moved-to-full-forum by a greater than one-third vote yes. A motion to
						move-to-full-forum takes precedence over a motion to approve but a motion to object can
						still return the House to debate. When a proposal is moved-to-full-forum, the Secretary
						should create a description of the issue of contention with input from one or more
						proponents and opponents. The Secretary would then publicly post the description and
						issue a full house ballot on the issue to be concluded before the next house meeting.
						The results of this vote would be final.
					</li>
					<li>
						A motion to force-move-to-full-forum works like to a motion to force-vote with two
						exceptions. First it requires only one-third support to proceed and second an
						uninterrupted motion to move-to-full-forum, not a motion to vote, is the result. For
						each proposal, no more than one such motion may be raised in any five minute period. A
						motion to force-move-to-full-forum takes precedence over a motion to force-vote.
					</li>
					<li>
						Proposals are tabled by a majority vote. A motion to table takes precedence over a
						motion to move-to-full-forum and a motion to approve but a motion to object can still
						return the House to debate. When a proposal is tabled, it is moved to the next house
						meeting.
					</li>
					<li>
						A motion to force-table entails an immediate and uninterrupted vote on house support for
						said motion. Should a majority agree with the motion, then an uninterrupted motion to
						table should proceed. For each proposal, no more than one such motion may be raised in
						any five minute period. A motion to force-table takes precedence over a motion to
						force-move-to-full-forum and a motion to force-vote.
					</li>
					<li>
						For all the motions above, the fraction of votes required is to be taken as a fraction
						of those electing to vote on the motion. Any members of the House who are not present or
						who choose to abstain shall not be included in the count of the total number of votes.
					</li>
					<li>
						A quorum of 10 voting members is required to hold a house meeting. Otherwise all
						proposals are tabled to the following house meeting.
					</li>
				</ol>
			</li>

			<li>
				House Executive Positions
				<ol class="list-decimal mx-4 px-4 py-2">
					<li>
						The House elects one individual to serve as Chair of the House, one individual to serve
						as Secretary, one individual to serve as Treasurer, and one individual to serve as
						President, each for a one year term.
					</li>
					<li>
						The Chair of the House is the chief administrator of the House's internal affairs. The
						Chair is responsible for: (a) creating and distributing to all members a schedule of
						House meetings for each academic semester, prior to the start of that semester; (b)
						explaining to the House how House meetings work at the start of each semester; (c)
						adding proposals to the agenda of the upcoming House meeting; (d) creating and
						distributing the agenda for each House meeting at least one day prior to the start of
						that meeting; (e) facilitating and maintaining order at all meetings of the House,
						following an established set of procedures and conducting meetings in a fair way that
						allows all members an opportunity to speak; (f) conducting elections; (g) generally
						overseeing the business of the House and directly overseeing the Historian, Social
						Chairs, Facilities, Inventory, and Sustainability Chair (FISCHair, pronounced Fish
						Chair), Technology Chair and the Rooming Chairs (also known as "Scootah Hockey Draft
						Moderatahs").
					</li>
					<li>
						The President is the chief diplomat of Simmons Hall and the administrator of the House’s
						external affairs. The President is responsible for: (a) being the primary representative
						of the House to any outside groups, including, but not limited to, the Simmons’ caterer,
						other dormitories and living groups, student governments, other student groups, MIT
						administration, MIT faculty, and groups outside MIT; (b) presenting the official opinion
						of the House, as determined by the House, to the aforementioned groups; (c) advocating
						and negotiating on behalf of Simmons Hall to the aforementioned groups; (d) presenting
						items for discussion and decision by the House on behalf of the aforementioned outside
						groups; (e) representing Simmons Hall to the Dormitory Council and casting votes on
						behalf of Simmons Hall; (f) communicating regularly with the Heads of House; (g)
						reporting regularly to the House; (h) inviting outside guests to address the House; (i)
						directly overseeing the FROSH Chairs, Athletics Chair, and Publicity Chair.
					</li>
					<li>
						The President shall have the power to convene Ad-Hoc Committees to assist him or her in
						the execution of the responsibilities of the office. Should the President believe it
						necessary, he/she has the power to form an Ad-Hoc Judicial Committee. The Chair of these
						committees, if not the President himself/herself must be approved by a majority vote of
						the House. Should the President be accused, the Chair of the House has the power to
						convene an Ad-Hoc Judicial Committee.
					</li>
					<li>
						The Secretary is responsible for: (a) documenting all business of the House; (b) keeping
						a public record of all decisions made by the House; (c) maintaining an archive to ensure
						that such documentation is available to all present and future House members; (d)
						maintaining a public record of the events and discussions that happen in the House and
						presenting those records at every house meeting; (e) running full forum votes by
						publicly posting a description of the measure(s) being voted on, then issuing a ballot
						on said measures to be concluded before the next house meeting; (f) serving in lieu of
						the Chair of the House if the Chair is temporarily unable to serve.
					</li>
					<li>
						The Treasurer manages the finances of the House and implements the financial decisions
						made by the House. The Treasurer is responsible for: (a) receiving signatory privileges
						for all House bank accounts, including the student activity account maintained by MIT;
						(b) keeping record of all financial decisions made by the House; (c) reimbursing
						individuals for House-related expenses approved by the House; (d) reporting at every
						meeting of the House, presenting the balances of all House bank accounts and an itemized
						report of all account activity, and making its reports available for viewing by all
						House Members; (e) Treasurer’s report should include: current balances, anticipated
						budgets, and effects of current proposals on the budget.
					</li>
					<li>
						If the Chair position becomes vacant, the President assumes the responsibilities of the
						Chair until a special election can be held to fill the position. Similarly, if the
						Presidency becomes vacant, the Chair assumes the responsibilities of the President until
						a special election can be held. In both cases, the special election must be announced
						immediately and run in compliance with the procedures for regularly scheduled elections.
					</li>
				</ol>
			</li>
		</ol>
	</li>

	<li>
		<p class="font-bold uppercase">Committee Chairs of the House</p>
		<ol class="list-decimal mx-4 px-4 py-2">
			<li>
				Committee Definitions and Membership
				<ol class="list-decimal mx-4 px-4 py-2">
					<li>
						A committee is a group of House members who collectively focus on particular sets of
						issues of importance to Simmons Hall. Committees may be established on a Standing or
						Ad-Hoc basis.
					</li>
					<li>
						An Ad-Hoc Committee for any purpose may be convened by the President or by virtue of a
						majority vote of the House.
					</li>
					<li>
						Any Standing Committee must be formed by action of the House in the form of an amendment
						to this Constitution and included in the following section.
					</li>
					<li>
						There are fifteen Committee Chairs leading the Standing Committees. They are: (a) the
						Social Chairs; (b) the Technology Chair; (c) the Facilities, Inventory, and
						Sustainability Chair (FISCHair, pronounced Fish Chair); (d) the Electrical Engineering
						Lab Chair; (e) the Library Chair; (f) the Entertainment Chair; (g) the Reservations
						Chair; (h) the Workshop Chair; (i) the Rooming Chairs; (j) the FROSH Chairs; (k) the
						Athletics Chair; (l) the Publicity Chair; (m) the Historian; (n) the Kitchen Chair; the
						Gym Chair (o); the Craft Room Chair (p). These Chairs and their committees must exist at
						all times, and are elected for a one year term. The roles and responsibilities of these
						committees are stated in this Article of the Simmons Hall Constitution.
					</li>
					<li>
						Each Committee Chair is responsible for: (a) maintaining a committee membership list;
						(b) scheduling and facilitating meetings of the committee; (c) reporting on the
						activities of the committee at House meetings. If the Chair(s) fail to report at House
						meetings, the Chair of the House may choose either: (a) to allow the remaining committee
						members to elect a new Chair; (b) to appoint a new Chair; (c) to dissolve the committee.
					</li>
					<li>
						Unless otherwise specified below, the House elects an individual or a ticket of two
						individuals to serve one-year terms as Chair of a Committee of the House defined in this
						Article. These Chairs are considered House Officers.
					</li>
					<li>
						Membership on any committee must be open to all members of the House. A member of the
						House may join a committee by contacting the Committee Chair or the Chair of the House
						and asking to be added to the membership list.
					</li>
					<li>
						If a committee member fails to report to meetings or otherwise fails to carry out the
						responsibilities required of the individual by that committee, the Committee Chair may
						choose to remove that individual from the membership of the committee.
					</li>
				</ol>
			</li>

			<li>
				Committee Procedures
				<ol class="list-decimal mx-4 px-4 py-2">
					<li>
						All business of each committee is discussed at meetings that are open and announced to
						all members of the committee, and preferably announced to all Members of the House.
						Committees may encourage discussion outside of meetings; however, all modes of
						discussion must allow for participation from every member of the committee.
					</li>
					<li>
						Committee meetings should be held at appropriate times given the particular topic of the
						committee's attention. Committee meetings should typically occur once each month, unless
						there is no business to be discussed at that time.
					</li>
					<li>
						Committees make decisions by group consensus or by majority vote. No decisions are made
						until after the full membership of the Committee is given a chance to comment and/or
						vote. Committee decisions are reported to the House by the Committee Chair. If any
						member of a committee disputes a decision reported by the Committee Chair, the Chair of
						the House may call a vote of the committee membership.
					</li>
				</ol>
			</li>

			<li>
				Role and Responsibilities of the Social Chairs
				<ol class="list-decimal mx-4 px-4 py-2">
					<li>
						At each election, the House will elect two individuals to serve as Social Chairs for
						one-year terms. The House shall constantly have two Social Chair positions. Should the
						number of Social Chairs drop below two, the House shall fill the vacant spot(s) in
						accordance with the replacement protocol outlined in Article IV, Section 1, Paragraph 9.
					</li>
					<li>
						The Social Chairs lead the Social Committee comprised of all lounge representatives.
					</li>
					<li>
						The Social Chairs are responsible for regularly reporting to the House Chair and the
						House.
					</li>
					<li>
						The Social Committee is responsible for organizing several House wide events each term.
						In addition, the Social Committee is responsible for interfacing with lounges and their
						representatives and ensuring that the lounge system is operating correctly.
					</li>
					<li>
						The Social Chairs shall have the power to create Social Committee subcommittees to
						organize more specific or long-term social events and functions.
					</li>
				</ol>
			</li>

			<li>
				Role and Responsibilities of the Technology Chair
				<ol class="list-decimal mx-4 px-4 py-2">
					<li>
						The Technology Chair leads the Technology Committee that maintains the computing
						resources managed by the House.
					</li>
					<li>
						The Technology Chair is responsible for (a) organizing, scheduling and running meetings
						of the Technology Committee; (b) ensuring that the Technology Committee accomplishes its
						goals; (c) reporting to the House Chair; (d) reporting regularly to the House.
					</li>
					<li>
						The Technology Committee is responsible for: (a) debugging and updating the Simmons
						Database System, House mailing lists, and House web server; (b) proposing, implementing,
						and maintaining technological solutions to meet and improve other House computing needs.
					</li>
					<li>
						The Technology Committee must maintain high standards of confidentiality and ethics when
						dealing with the Simmons database and sensitive issues.
					</li>
				</ol>
			</li>

			<li>
				Role and Responsibilities of the Facilities, Inventory, and Sustainability Chair (FISCHair,
				pronounced Fish Chair)
				<ol class="list-decimal mx-4 px-4 py-2">
					<li>
						The FISCHair leads the Facilities Committee comprised of the Electrical Engineering Lab
						Chair, the Library Chair, the Entertainment Chair, the Reservations Chair, the Workshop
						Chair, the Kitchen Chair, and the Gym Chair.
					</li>
					<li>
						The Facilities Chair is responsible for (a) organizing, scheduling and running meetings
						of the Facilities Committee; (b) ensuring that the Electrical Engineering Lab Chair, the
						Library Chair, the Entertainment Chair, the Reservations Chair, the Workshop Chair, the
						Kitchen Chair, and the Gym Chair fulfill their jobs to their fullest extent; (c)
						overseeing inventory reports from the above chairs (d) working with the House Manager to
						accomplish tasks; (e) creating a frequent (at least monthly) , publicly available
						sustainability report detailing the environmental impact of the House; (f) creating
						initiatives to improve said sustainability report; (g) reporting to the House Chair; (h)
						reporting regularly to the House.
					</li>
					<li>
						The inventory reports consist of names of items, quantities, last checked dates,
						monetary values, and if they are lost. These reports should occur on at least a monthly
						basis, in order to ensure Simmons property is well-accounted for.
					</li>
					<li>
						The Facilities Committee is responsible for creating, recommending, and overseeing the
						implementation of policies regarding the use of House-owned equipment and spaces.
					</li>
					<li>
						The sustainability report consists of changes in relevant metrics, graphs of said
						metrics over time, a resident-to-resident comparison to other dorms, updates on the
						impact of any recent sustainability initiatives, and a list of any current initiatives.
						Relevant metrics may include, but are not limited to, MTC02e (Metric Tons of CO2), waste
						created, energy usage and water usage. These reports ensure residents have a better
						understanding of their impact on the environment and what they can do to improve it.
					</li>
				</ol>
			</li>

			<li>
				Role and Responsibilities of the Electrical Engineering Lab Chair
				<ol class="list-decimal mx-4 px-4 py-2">
					<li>
						The Electrical Engineering Lab Chair is part of the Facilities Committee and maintains
						the lab facility on the seventh floor. The Electrical Engineering Lab Chair is
						responsible for (a) organizing training sessions for residents who want to use the lab
						(b) ensuring that residents are properly trained and certified to use the lab (c)
						reporting to the Facilities Chair; (d) reporting regularly to the House.
					</li>
				</ol>
			</li>

			<li>
				Role and Responsibilities of the Library Chair
				<ol class="list-decimal mx-4 px-4 py-2">
					<li>
						The Library Chair is part of the Facilities Committee and supervises the library on the
						second floor. The Library Chair is responsible for (a) creating and maintaining a
						checkout system for library materials; (b) organizing the library; (c) obtaining new
						books when the desire is expressed by the House; (d) reporting to the Facilities Chair;
						(e) reporting regularly to the House.
					</li>
				</ol>
			</li>

