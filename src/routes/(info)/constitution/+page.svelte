		<p class="font-bold uppercase">The House Support Team</p>
		<ol class="list-decimal mx-4 px-4 py-2">
			<li>
				Live-In House Support Positions
				<ol class="list-decimal mx-4 px-4 py-2">
					<li>
						MIT specially selects individuals to live in Simmons Hall in order to ensure the health,
						safety and well-being of residents and to promote a vibrant and diverse social life.
						These include Head of Houses and Graduate Resident Advisors (GRAs), Visiting Scholars,
						and the Assistant Director / Area Director<a id="link-to-f3" href="#f3" class="anchor"
							><sup>3</sup></a
						> among others. Along with the House Manager and staff, these individuals collectively form
						a House Support Team, with the Head of Houses serving as its head, that operates independently
						of the House government but must remain in contact with the House government in order to
						ensure a mutually supportive working relationship.
					</li>
					<li>
						Head of Houses, GRAs, and any other individuals who are specially selected by MIT to
						live in Simmons Hall are all considered Members of the House, but do not have any
						official standing within the House, nor do they have any official responsibilities to
						the House. Nonetheless, they are encouraged to participate in all activities of the
						House, and to offer their support where the House would benefit from the specialized
						assistance they may be able to provide.
					</li>
				</ol>
			</li>

			<li>
				The Head of House Role
				<ol class="list-decimal mx-4 px-4 py-2">
					<li>
						The Simmons Hall Head of House team consists of the Heads of House and Associate Heads
						of House, who are faculty members living with their families in Simmons Hall. The Heads
						of House and Associate Heads of House are appointed to their positions by the Office of
						the Dean for Student Life. They are expected to integrate fully into Simmons and to
						support the entire dorm socially and intellectually.
					</li>
					<li>
						The Head of House role in Simmons falls into four areas: (a) They hold frequent social
						events, and participate actively in the social events held by House residents, in order
						to get to know as many undergraduates as possible; (b) they help residents deal with
						problems, ranging from inter-personal or inter-group issues to those of a highly
						personal and individual nature; (c) they supervise the Graduate Resident Advisors; (d)
						they report to the rest of the MIT faculty and to the MIT administration.
					</li>
					<li>
						When a new Head of House or Associate Head of House must be selected, a committee of
						House Officers is formed to review candidates and make recommendations to the Dean for
						Student Life.
					</li>
				</ol>
			</li>

			<li>
				The Graduate Resident Advisor Role
				<ol class="list-decimal mx-4 px-4 py-2">
					<li>
						Graduate Resident Advisors are MIT graduate students hired by the Office of the Dean for
						Student Life to live in Simmons Hall, sometimes along with partners or families. They
						are expected to integrate fully into the dorm and to provide support to a specific set
						of residents living in a particular area of the building.
					</li>
					<li>
						The GRA role in the House falls into four areas: (a) They serve as advisors and mentors
						to residents, particularly those living within their area; (b) they respond to crises
						and conflicts that occur within the dorm, relying on their training and judgment; (c)
						they serve as advocates for the interests of students, as liaisons between students and
						Heads of House, and sometimes as liaisons between students and the administration; (d)
						they serve as community facilitators, encouraging interaction and providing recreational
						opportunities for residents, particularly those living within their area.
					</li>
					<li>
						The GRA role in the dorm expressly excludes any disciplinary component. Only the Heads
						of House may officially recommend disciplinary action against a resident of Simmons
						Hall.
					</li>
					<li>
						When new GRAs must be hired to fill open positions in Simmons Hall, a House committee of
						all interested Simmons residents and the Head of House team is formed to review all
						candidates and provide a list of recommended candidates to the Office of the Dean for
						Student Life.
					</li>
				</ol>
			</li>

			<li>
				Other Special Live-In Positions
				<ol class="list-decimal mx-4 px-4 py-2">
					<li>
						Other individuals who are selected to live in Simmons Hall may include Visiting Scholars
						and their families, Residential Life Associates, or possibly others to be defined in the
						future.
					</li>
					<li>
						Individuals specially selected by an MIT authority to live in Simmons Hall, while
						Members of the House, are not required to have any special responsibilities to the
						House. However, the House may establish House Policies regarding the roles these
						individuals are expected to play in the House. Live-in roles as defined by the House
						must complement, and must not contradict, the roles as defined by the MIT authority
						responsible for appointing or hiring the individuals.
					</li>
				</ol>
			</li>

