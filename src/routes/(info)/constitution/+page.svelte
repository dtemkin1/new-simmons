<h2 class="h2">The Constitution of Simmons Hall</h2>
<p>Last updated February 12, 2023</p>

<hr />

<p class="font-bold uppercase">Preamble</p>
<p>
	For decades at MIT, dormitory communities have been developing sophisticated systems of
	self-governance. These systems are entirely student-built and student-run, and they have received
	very little support or interference from the MIT administration. Despite or because of this (you
	be the judge), these governance systems have developed quite well.
</p>
<p>
	According to the MIT administration, Simmons Hall is intended to signal a change from the current
	standards of dorm life. Thisi is largely true. However, the governance of Simmons Hall will not be
	a change away from the self-governance model, but formalization and strengthening of it. The
	Simmons Hall governance model is intended to allow the dorm to have more independence and a
	stronger voice in the decisions that affect it. Moreover, it is intended to better define the
	relationship of Heads of House<a id="link-to-f1" href="#f1" class="anchor"><sup>1</sup></a> and
	Graduate Resident Tutors<a id="link-to-f2" href="#f2" class="anchor"><sup>2</sup></a> to the governance
	process, encouraging their participation and support but ensuring that they do not assume control over
	governance functions.
</p>
<p>
	The Simmons Hall governance model is also founded on the concept of the House as an entity that is
	more than just a building and more than simply the sum of its residents. All residents are
	supported by the House physically and socially. Reciprocally, the House requires support from all
	of its residents. This House model works on several basic principles:
</p>
<p>
	Principle 1: All residents have an implicit responsibility to the House. There is not a select
	group of people who handle all the functions of House governance and whom you can blame if dorm
	life is not what you want it to be. It is up to everyone. If you want Simmons Hall to be a good
	place, you have to get involved and make it that way. If you think Simmons Hall sucks, then
	there&#8217;s no one you can blame but yourself.
</p>
<p>
	Principle 2: There are varying levels of responsibility to choose from. You can run for an elected
	Office, or volunteer to serve on a committee, or show up to House meetings, or on the most basic
	level, have respect for the House and for your fellow residents.
</p>
<p>
	Principle 3: The House speaks for the House. When the House makes a decision, it is explicit that
	the decision was made through a democratic process that involves all House residents. There are no
	representatives who &#8220;call the shots&#8221; for everyone.
</p>
<p>
	Principle 4: If you do not like something, change it. While traditions are important to building
	culture, the House should not keep doing something simply because people did it the year before,
	and other people did it the year before that. Times keep changing and the House should keep
	thinking creatively and coming up with new ways to improve House life.
</p>
<p>
	Principle 5: House governance should be part of the educational experience for all residents.
	Students and faculty alike should feel that they learned something from being an active member of
	the House. They should also feel that they had some degree of fun and that their educational
	experience was better because they had a release from the rigor of MIT academics and research.
</p>
<p>This is the philosophy behind the governance of Simmons Hall.</p>
<p>
	Jeff Roberts<br />
	Simmons Hall Founders Group, 1999-2002<br />
	Governance Subcommittee Chair<br />
	September 3, 2002
</p>

