	<li>
		<p class="font-bold uppercase">Officer Provisions</p>
		<ol class="list-decimal mx-4 px-4 py-2">
			<li>
				Elections
				<ol class="list-decimal mx-4 px-4 py-2">
					<li>
						Only undergraduate members of the House are eligible to serve as officers. No individual
						may hold more than one office simultaneously, except in cases where an office remains
						unfilled for an extended length of time.
					</li>
					<li>
						All elections must be open to all members of the House, whether or not they are able to
						attend House meetings.
					</li>
					<li>
						The election process is overseen by the Elections Organizer. By default, the Elections
						Organizer is the Chair of the House. However, if the Chair intends to be a candidate in
						the election or otherwise wishes not to be Elections Organizer, the Chair must select
						another undergraduate member of the House (who must not intend to be a candidate) to be
						the Elections Organizer. This selection must be approved by a simple majority vote of
						the House. The selection of Elections Organizer must occur before the beginning of
						nominations. The Elections Organizer cannot be nominated to be a candidate. The
						Elections Organizer hears all complaints related to the fairness of the election
						procedure.
					</li>
					<li>
						Nominations for officer positions will occur during the week preceding the elections
						House meeting. A nominee will be placed on the ballot if their name is mentioned. People
						can nominate themselves and nominate multiple times. One can be nominated by proxy. The
						Chair must nominate anyone who notified him/her by two days before the House meeting.
					</li>
					<li>
						During the week before the announced election meeting and the final meeting of the term,
						there is a special election meeting. During this meeting, all candidates will be given
						three minutes to present their platform and one minute to answer questions. A further
						several minutes will be dedicated to questioning any/all of the candidates for a
						particular office. Proxies may read the platform of missing candidates, but may not take
						part in the question answering sessions.
					</li>
					<li>
						The voting period is the period after the elections meeting and before the agenda closes
						for the next regularly scheduled house meeting. The winner of each election is
						determined using instant runoff voting. In the case of a tie between two individuals for
						a position that can be held by two people, they may chose to serve as co-chairs or to
						hold a runoff election.
					</li>
					<li>
						New officers take office during the first meeting of the spring term. During the
						transition period (the time between election results and the first spring House
						meeting), outgoing officers have the responsibility of training the incoming officers
						and handing over turnover files. Outgoing officers also have the responsibility of
						helping to run the first meeting of the spring term.
					</li>
					<li>
						An office becomes vacant when the individual holding that office resigns or is no longer
						an official resident of Simmons Hall. An Officer may resign by informing the Chair of
						the House of one’s resignation, or the Chair of the House may assume that an officer has
						resigned if that officer fails to communicate with the House or otherwise fails to
						undertake the responsibilities of that office.
					</li>
					<li>
						If a House Officer position, other than one specified in article II.5.7, becomes vacant
						mid-term, the resigning officer should be reminded of his/her duty to train the
						replacement. The Chair of the House should notify the House that the spot has become
						vacant and that they may demonstrate interest within the week. At the end of the week,
						one of two outcomes has occurred: (a) 1 or 0 people have contacted the chair. In this
						case, the chair is permitted to appoint said person (if one is found) to the office in
						question. The Chair must ratify the selection at the next House meeting with a simple
						majority vote; (b) 2 or more people have contacted the chair. In this case, the chair
						(with the help of other officers) must administer a one-week mid-term election for the
						House to select between the candidates. Additionally, if the end of the one-week special
						election would fall within one month of the end of a regular election, then the House
						Chair may exercise discretion in deciding whether to run a vote / appoint directly /
						wait for the main election.
					</li>
					<li>
						The previous section, Article 5 Section 1 Item 8, does not apply to the instance in
						which the Secretary chooses to assume the role of House Chair permanently.
					</li>
					<li>
						Provisionally elected officers take office immediately upon election and fill the vacant
						office until the time of the next regular election.
					</li>
				</ol>
			</li>

			<li>
				Recall Elections
				<ol class="list-decimal mx-4 px-4 py-2">
					<li>
						Any member or group of members of the House may propose a Recall Election for a specific
						Office, if that member or group feels that: (a) the current holder of the Office is
						neglecting the responsibilities of that office; (b) the current holder of the Office is
						abusing the authority granted to that office; (c) the current holder of the Office is
						not acting in the interest of the House. A proposal of a Recall Election must include an
						explanation that addresses at least one of these three points. The proposal is placed on
						the agenda at the discretion of the House Chair, given that it includes this
						explanation.
					</li>
					<li>
						A proposal of a Recall Election is discussed and voted upon in the same manner as is any
						other item of House business.
					</li>
					<li>
						If the Chair of the House is the object of the Recall Election, the Secretary of the
						House assumes the responsibilities of the Chair for all business relating to the Recall
						Election.
					</li>
					<li>
						If a Recall Election is approved by a House vote, the Officer in question holds the
						position until it is filled according to the Special Elections procedures outlined in
						Section IV.1.9-11.
					</li>
					<li>
						The holder of the Office in question before the Recall Election is eligible to run in
						the resulting Special Election, unlike in Impeachment proceedings.
					</li>
				</ol>
			</li>

			<li>
				Impeachment
				<ol class="list-decimal mx-4 px-4 py-2">
					<li>
						Any member or group of members of the House may propose an Article of Impeachment
						against an officer, if that member or group feels that: (a) the officer is neglecting
						the responsibilities of that office; (b) the officer is abusing the authority granted to
						that office; (c) the Officer is not acting in the interest of the House. A proposed
						Article of Impeachment must include an explanation that addresses at least one of these
						three points.
					</li>
					<li>
						A proposed Article of Impeachment is placed on the House agenda, discussed, and voted
						upon in the same manner as is any other item of House business.
					</li>
					<li>
						If the Chair of the House is the object of the Article of Impeachment, the Secretary of
						the House assumes the responsibilities of the Chair for all business relating to the
						Article of Impeachment.
					</li>
					<li>
						If an Article of Impeachment is approved by a House vote, the Chair of the House holds a
						hearing open to all members of the House. The proponent of the Article of Impeachment,
						the object of the Article of Impeachment, and the Chair are given the opportunity to
						speak at this hearing, and may invite witnesses to speak on their behalf, but any
						witness may be dismissed by the Chair of the House on the grounds of irrelevance. No
						other individuals are allowed to speak at this hearing.
					</li>
					<li>
						The Chair of the House may decide, based on the evidence presented at the hearing, to
						remove an individual from office if and only if: (a) the information presented in the
						Article of Impeachment is truthful in content; and (b) the Article of Impeachment
						sufficiently explains why the impeached individual should be removed from office. After
						deliberation, the Chair presents the decision, along with a written explanation, to the
						House.
					</li>
					<li>
						An office that is unoccupied as the result of impeachment proceedings is then treated in
						the same manner as any other vacancy, except that the removed officer is no longer
						eligible to hold that office.
					</li>
				</ol>
			</li>
		</ol>
	</li>

