<h2 class="h2">Reserving the Multipurpose Room</h2>
<p>
	For non-Simmons groups who wish to reserve the Multipurpose Room and do not have a current
	resident to act as a point of contact, please contact House Manager Nika Hollingsworth (<a
		class="anchor"
		href="mailto:nika_h@mit.edu">nika_h@mit.edu</a
	>) to register the event and the Reservations Chair (<a
		class="anchor"
		href="simmmons-reservations@mit.edu">simmons-reservations@mit.edu</a
	>) to confirm availability.
</p>

<p>The Multipurpose Room has a maximum capacity of 125.</p>

<p>
	Reservations must be made at least a week in advance. Additionally, they can only be made
	beginning six weeks before the semester containing the requested dates. Events that span multiple
	days require approval at a House meeting, which is held every other Sunday night during the
	semester.
</p>

<p>
	All guests will need an MIT ID or a valid photo ID to gain access to the building. An account
	number must also be provided to the House Manager so a trash fee can be applied in the event the
	space is left in a less than desirable state.
</p>
