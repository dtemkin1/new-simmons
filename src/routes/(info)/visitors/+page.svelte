<h2 class="h2">Tours</h2>
<p>
	In recognition of the architectural significance of Simmons Hall, we offer tours of the
	non-residential areas of the building. A tour of Simmons Hall usually takes 45 to 75 minutes,
	depending upon the interests of the tour group. The maximum tour group size is 20 people, however,
	given enough notice, Simmons may be able to arrange for additional guides to accommodate larger
	groups.
</p>

<p>
	Tours cost US$5 per person in the tour group, by exact cash or by check made out to MIT. This fee
	is sometimes waived, as is the case for members of the MIT community, MIT alums and special guests
	of MIT.
</p>

<p>
	As our tour guides are students, we may not be able to accommodate your request for a tour. For
	your best chances of receiving a tour, please email
	<a class="anchor" href="mailto:simmons-tours@mit.edu">simmons-tours@mit.edu</a>
	approximately two weeks before your requested tour date to start a conversation with our guides.
</p>

<p>
	Additionally, we are unfortunately unable to offer tours outside of the regular academic calendar,
	including during the summer (May through August) or around the holidays (December through
	January). Please take this into account when submitting a tour request. We look forward to seeing
	you!
</p>

<p>
	Contact <a class="anchor" href="mailto:simmons-tours@mit.edu">simmons-tours@mit.edu</a>
	for more information.
</p>
