<h1 class="h1">Lounge Bylaws</h1>
<p>
	Contact our Social Chairs at <a class="anchor" href="mailto:simmons-social-chair@mit.edu">
		simmons-social-chair@mit.edu</a
	> for more information.
</p>
<hr />
<ol class="list-decimal list-outside ml-6 py-2">
	<li>
		<p class="font-bold uppercase">Purpose</p>
		<p>
			Simmons Hall is not conducive to the traditional floor or house divisions present in other
			dorms. Lounges membership is aimed at creating smaller in-house groups that will contribute to
			the over all social scene within the dorm.
		</p>
	</li>
	<li>
		<p class="font-bold uppercase">Lounge Setup</p>
		<ol class="list-decimal list-outside ml-6 py-2">
			<li>
				Lounge social groups become officially recognized upon receipt of funding from the house.
				Official recognition expires if it is not renewed by the receipt of more funds at the second
				house committee meeting of each term.
			</li>
		</ol>
	</li>
	<li>
		<p class="font-bold uppercase">The Signup Process</p>
		<ol class="list-decimal list-outside ml-6 py-2">
			<li>
				The lounge committee chair shall be responsible for ensuring that the signup process runs
				smoothly and is effectively publicized to the house.
			</li>
			<li>
				Lounge signup shall take place via an online form in the Simmons Database. The lounge
				committee chair (with the help of a member of the IT committee, if necessary) shall make
				sure this form is set up properly and updated in a timely fashion.
			</li>
			<li>
				One day prior to the first house committee meeting of the term shall be the new lounge
				deadline. Lounges not included in the online form that submit their information by this
				deadline must be added to the form within four days of the deadline. New lounges must submit
				the following to the lounge committee chair:
				<ol class="list-decimal list-outside ml-6 py-2">
					<li>Lounge social group name</li>
					<li>Lounge representative</li>
					<li>Lounge URL (if the group has one)</li>
					<li>Lounge Blurb: a paragraph representing the social group to be posted online</li>
					<li>A list of at least 5 usernames of members.</li>
				</ol>
			</li>
			<li>
				One week after the first house committee meeting of the term shall be the membership
				deadline. At the end of this day, the lounge committee chair shall send official membership
				lists to all the lounge representatives.
			</li>
			<li>
				Late membership deadlines shall occur one day prior to the funding deadline for later house
				committee meetings. At the end of this day, the lounge coordinator need only send official
				membership lists to new lounge representatives.
			</li>
		</ol>
	</li>
	<li>
		<p class="font-bold uppercase">Membership and Funding</p>
		<ol class="list-decimal list-outside ml-6 py-2">
			<li>
				Every resident of Simmons Hall is eligible to become a member of one lounge of his or her
				choice during any given term. Between the membership deadline and the beginning of the next
				term, lounge members may not join a different lounge.
			</li>
			<li>
				To receive funding, a lounge social group must request it from the house committee. Only
				lounge social groups with at least five members and a lounge that meets the requirements of
				Section II are eligible to apply.
			</li>
			<li>
				Funding requests from all lounge social groups must be voted on at the second house
				committee meeting of the term. Lounges funded at this point will receive $30 in house funds
				for the term per member signed up by the membership deadline.
			</li>
			<li>
				If a resident unaffiliated with any lounge joins a lounge after the membership deadline but
				prior to the late membership deadline, that lounge may request an additional $30 in house
				funds from the house committee for that term.
			</li>
			<li>
				If after the second house committee meeting of the term a group of five or more people wish
				to start one up, they may request $30 from house funds for that term per person signed up by
				the late membership deadline.
			</li>
			<li>
				No lounge social group may receive more than $1000 in house funds per term no matter how
				high its membership levels. However, the house committee may choose to allocate additional
				funds to specific house-wide proposals (events, trips, etc.) from lounge members.
			</li>
			<li>
				Funds allocated to a lounge social group one term roll over from fall to spring Term, but
				not spring to fall, provided that the lounge social group does not let its official
				recognition expire during the new term.
			</li>
		</ol>
	</li>
	<li>
		<p class="font-bold uppercase">Lounge Government</p>
		<ol class="list-decimal list-outside ml-6 py-2">
			<li>
				Each lounge must designate one representative to be in charge of requesting funding from the
				house committee and signing receipts for submission to the house treasurer. Lounge members
				will not be reimbursed for lounge expenses unless their lounge's representative signs their
				receipts
			</li>
			<li>
				The lounge representative shall be selected by an election open to all lounge members at the
				beginning of each term. The result of all lounge representative elections must be reported
				to the lounge coordinator by the new lounge deadline.
			</li>
			<li>
				At any time during the term, three lounge members may call for a vote of &quot;no
				confidence&quot; in their lounge representative. If a majority of lounge members vote
				&quot;no confidence,&quot; the lounge representative shall be removed from office, and a new
				election shall be held.
			</li>
			<li>
				Other aspects of lounge government (how a budget is agreed upon, how voting takes place,
				etc.) shall be left up to the individual lounges.
			</li>
			<li>The lounge committee chair cannot be the representative of any lounge.</li>
		</ol>
	</li>
	<li>
		<p class="font-bold uppercase">Use of Funds</p>
		<ol class="list-decimal list-outside ml-6 py-2">
			<li>
				Lounge social groups may use lounge funds for events provided that all lounge members are
				aware of this fact at least two days in advance. Additionally, the event must be approved by
				the larger of 3 members or 1/3 of the entire group. These members do not need to commit to
				attending the event, but they must indicate that they approve of the expenditure. Approval
				shall consist of a record in a publicly archived electronic form.
			</li>
			<li>
				Trips using lounge funds must be able to accommodate all interested members of the lounge.
				Such trips must be advertised to the lounge membership at least two days before the signup
				deadline.
			</li>
			<li>
				Lounge social groups may purchase items to be placed in their lounge. A lounge social group
				which purchases capital improvements may choose the permanent placement of the purchase in a
				public area. However, any capital improvements shall be the property of the house.
			</li>
			<li>
				The maximum that a group will be reimbursed for an approved event will be the group's
				per-person allocation times the number of members who commit to attending the event.
			</li>
			<li>
				Before a lounge expenditure can be reimbursed it must be reported on the Simmons DB and the
				lounge coordinator must verify the event was in compliance with the lounge bylaws. The
				report to the Simmons DB must include a description of the event, the amount spend, and the
				number of lounge members in attendance.
			</li>
		</ol>
	</li>
	<li>
		<p class="font-bold uppercase">First Time Caveat</p>
		<p>
			To make up for lost time, all lounges people are interested in starting shall be funded $150,
			effective as soon as five members sign up. Lounges may request funding for additional members
			at the $30 level at the house committee meeting directly following the one at which this
			document is approved. For funding purposes, that meeting shall function like the second house
			committee meeting of the term.
		</p>
	</li>
</ol>

