<h1 class="h1">Lounge Bylaws</h1>
<p>
	Contact the Social Chairs at <a class="anchor" href="mailto:simmons-social-chair@mit.edu">
		simmons-social-chair@mit.edu</a
	> for more information.
</p>
<hr />
<ol class="list-[upper-roman] list-outside ml-6 py-2">
	<li>
		<h2 class="font-bold uppercase">Purpose</h2>
		<p>
			The structure of Simmons Hall is not conducive to traditional college floor dynamics due to
			the existence of closed lounges and floor separations. The purpose of lounge membership is to
			create smaller in-house groups that will contribute to the overall social scene within the
			dorm.
		</p>
	</li>
	<li>
		<h2 class="font-bold uppercase">Lounge Recognition</h2>
		<p>
			Lounge groups become officially recognized upon registration in the Simmons Database, as long
			as they meet the necessary conditions listed in Section III of this document. They will
			receive funding by the second house committee meeting of each semester, unless they fail to
			meet the aforementioned conditions, in which case their lounge group shall expire.
		</p>
	</li>
	<li>
		<h2 class="font-bold uppercase">Condition for Lounge Recognition</h2>
		<ol class="list-[upper-roman] list-outside ml-6 py-2">
			<li>Lounges must have no less than 5 members.</li>
			<ol class="list-decimal list-outside ml-6 py-2">
				<li>
					Allowances will be made for the period between the start of each semester and the second
					house meeting of that semester, so that new members may be recruited through rush or other
					means.
				</li>
			</ol>
			<li>
				Lounges must make a reasonable effort to increase social involvement and inclusivity within
				Simmons at least once a semester. This may include:
				<ol class="list-decimal list-outside ml-6 py-2">
					<li>Participating in fall and/or spring lounge rush, or</li>
					<li>
						Increasing lounge membership by at least one member over the course of a semester, or
					</li>
					<li>
						Running a lounge event that is publicly advertised to non-lounge members, through
						posters, emails, etc.
						<ol class="list-[lower-alpha] list-outside ml-6 py-2">
							<li>
								Funds for said event may be requested from the house committee or social chair(s)
								and need not come directly from lounge funds.
							</li>
							<li>The scope of potential attendees must be dorm-wide.</li>
						</ol>
					</li>
				</ol>
			</li>
			<li>
				Lounges must complete all reasonable requests made by Social Chairs.
				<ol class="list-decimal list-outside ml-6 py-2">
					<li>
						A reasonable request is defined as a task the lounge or lounge representative can do in
						both the allotted amount of time, and without defying policies set by MIT or Simmons
						Hall.
					</li>
					<li>
						If a lounge member feels a request is unreasonable, they may first contact Social Chairs
						to communicate issues and find a reasonable compromise. If a reasonable compromise
						cannot be found, the lounge member or representative may contact a member of the house
						administration - the active Area Director, Associate Head(s) of House, or Head(s) of
						House, to find a solution.
					</li>
				</ol>
			</li>
		</ol>
	</li>
	<li>
		<h2 class="font-bold uppercase">The Signup Process</h2>
		<ol class="list-decimal list-outside ml-6 py-2">
			<li>
				The social chair(s) shall be responsible for ensuring that the signup process runs smoothly
				and is effectively publicized to the house.
			</li>
			<li>
				Lounge signup shall take place via an online form in the Simmons Database. The social
				chair(s) (with the help of a member of the IT committee, if necessary) shall make sure this
				form is set up properly and updated in a timely fashion.
			</li>
			<li>
				The day after Simmons Hall Lounge Rush will be the start of both the lounge registration
				period and the lounge member reassignment period (in reference to when residents may join
				and/or leave lounges).
			</li>
			<li>
				New lounges must submit the following to the lounge creation form:
				<ol class="list-decimal list-outside ml-6 py-2">
					<li>Lounge social group name</li>
					<li>Lounge representative</li>
					<li>A list of at least 5 usernames of members</li>
					<li>A lounge description.</li>
				</ol>
			</li>
			<li>
				Seven days after Simmons Hall Lounge Rush, the lounge registration period will end, and
				lounges shall be uploaded by the social chair(s) 1-2 days after that end date. The
				opportunity for lounge member reassignment will end ten days after Simmons Hall Lounge Rush
				to allow for students to add themselves to newly created lounges.
			</li>
			<li>
				Social Chairs, at any point, may make extensions to the length of the add period if it is
				done with the intention of improving the lounge signup process for all members.
			</li>
			<li>
				Social Chairs, under any circumstances, may <em class="italic">not</em> open lounge signups
				to any students after lounge funds are allocated.
				<ol class="list-decimal list-outside ml-6 py-2">
					<li>
						For newly created lounges, defined as lounges created during the current semester’s add
						period, if all members listed in the lounge creation form did not join within the add
						period, funds may not be allocated until all individuals join. In this case, an
						authorized admin may either use administrative powers to add the designated individuals
						to their lounge, or enable lounge sign ups during a specified ten minute period to allow
						the particular users to join.
						<ol class="list-[lower-alpha] list-outside ml-6 py-2">
							<li>
								No other users (users not listed in the respective lounge’s signup form) may join
								during this ten minute period. Social Chairs reserve the right to remove any
								unauthorized members from the lounges they join.
							</li>
						</ol>
					</li>
				</ol>
			</li>
		</ol>
	</li>
	<li>
		<h2 class="font-bold uppercase">Membership and Funding</h2>
		<ol class="list-decimal list-outside ml-6 py-2">
			<li>
				Every resident of Simmons Hall is eligible to become a member of one lounge of their choice
				during any given term. Between the membership deadline and the beginning of the next term,
				lounge members may not join a different lounge.
			</li>
			<li>
				Lounges who meet the required conditions after the lounge member reassignment period ends
				will be allocated funds equivalent to $30 times the membership count of said lounge.
			</li>
			<li>
				No lounge social group may receive more than $1,000 in house funds per term no matter how
				high its membership levels. However, the house committee may choose to allocate additional
				funds to specific house-wide proposals (events, trips, etc.) from lounge members.
			</li>
			<li>
				Funds allocated to a lounge social group one term roll over from fall to spring Term, but
				not spring to fall, provided that the lounge social group does not let its official
				recognition expire during the new term.
			</li>
		</ol>
	</li>
	<li>
		<h2 class="font-bold uppercase">Use of Funds</h2>
		<ol class="list-decimal list-outside ml-6 py-2">
			<li>
				Lounge social groups may use lounge funds for events provided that all lounge members are
				aware of this fact at least two days in advance. Additionally, the event must be approved by
				the larger of 5 members or 1/3 of the entire group. These members do not need to commit to
				attending the event, but they must indicate that they approve of the expenditure. Approval
				shall be requested via a lounge event proposal in the Simmons DB.
			</li>
			<li>
				Trips using lounge funds must be able to accommodate all interested members of the lounge.
				Such trips must be advertised to the lounge membership at least two days before the signup
				deadline.
			</li>
			<li>
				Lounge social groups may purchase items to be placed in a lounge. However, any capital
				improvements shall be the property of the house.
			</li>
			<li>
				The maximum that a group will be reimbursed for an approved event will be the group's
				per-person allocation times the number of members who commit to attending the event.
			</li>
			<li>
				Before a lounge expenditure can be reimbursed it must be reported on the Simmons DB and the
				Social Chair must verify the event was in compliance with the lounge bylaws. The report to
				the Simmons DB must include a description of the event, the amount spent, and the number of
				lounge members in attendance.
			</li>
		</ol>
	</li>
	<li>
		<h2 class="font-bold uppercase">Removal of Lounge Members</h2>
		<p>
			In the event that lounges have serious interpersonal disputes that cannot be resolved among
			members, members may meet with the active Area Director, Associate Head(s) of House, and/or
			Head(s) of House to discuss removing a member from the lounge. Social Chairs are expected to
			enact the rationale and advice provided by one of the administrative members above. Social
			Chairs are also encouraged to give contacts for Ombuds, VPR, IDHR, etc. if advised so by house
			administration.
		</p>
	</li>
	<li>
		<h2 class="font-bold uppercase">In Case of Technological Issues</h2>
		<p>
			If technical errors prevent use of official channels within the DB for joining lounges,
			submitting or voting on lounge event proposals, etc., students may contact Social Chairs
			directly and be provided with an external solution until said technical errors can be
			resolved.
		</p>
	</li>
</ol>

